@UsesStroblMocks
final class Test02NonStroblMock: XCTestCase {
    
    @StroblMock var mock: NonStroblCustomReflectableMock!
    
    override func setUpWithError() throws {
        mock = NonStroblCustomReflectableMock()
    }
    
    func testNonStroblCustomReflectableMockCausesVerifyStroblMocksUnusedToFailWithNonStroblMockMessage() {
        let expectedDescription = "failed - 'mock' does not appear to be a Strobl Mock. Neither 'calledMethods' or 'calledStaticMethods' properties were found."
        let options = XCTExpectedFailure.Options()
        options.issueMatcher = { issue in
            issue.type == .assertionFailure && issue.compactDescription == expectedDescription
        }

        // Test needs to be run to get failure.
        // It should result in the following message:
        //
        //   'mock' does not appear to be a Strobl Mock. Neither 'calledMethods' or 'calledStaticMethods' properties were found.
        //
        XCTExpectFailure("This text exercises code generated by @UsesStroblMocks and should fail", options: options)
        verifyStroblMocksUnused()
        
    }

}
